% --- Executes on selection change in popupmenu1.
function popupmenu1_Callback(hObject, eventdata, handles)
% hObject    handle to popupmenu1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hints: contents = cellstr(get(hObject,'String')) returns popupmenu1 contents as cell array
%        contents{get(hObject,'Value')} returns selected item from popupmenu1

N=str2double(get(handles.edit1,'string'));
fs=str2double(get(handles.edit2,'string'));
temps=(0:N-1)/fs;
freq=(-N/2:N/2-1)/N*fs;
val=get(handles.popupmenu1,'value');
switch val
    case 1   %cos
        prompt={'Entrer amplitude :','Entrer la frequence:'};
        name='parametre du signal';
        numlines=1;
        defaultanswer={'1','1000'};
        
        answer=inputdlg(prompt,name,numlines,defaultanswer);
        
        A=str2double(answer(1,1));
        F=str2double(answer(2,1));
        sig=A*cos(2*pi*F*temps);
    case 2   %sin
        prompt={'Entrer amplitude :','Entrer la frequence:'};
        name='parametre du signal';
        numlines=1;
        defaultanswer={'1','1000'};
        
        answer=inputdlg(prompt,name,numlines,defaultanswer);
        
        A=str2double(answer(1,1));
        F=str2double(answer(2,1));
        sig=A*sin(2*pi*F*temps);
    case 3   %sinc
        prompt={'Entrer amplitude :','Entrer la frequence:'};
        name='parametre du signal';
        numlines=1;
        defaultanswer={'1','1000'};
        
        answer=inputdlg(prompt,name,numlines,defaultanswer);
        
        A=str2double(answer(1,1));
        F=str2double(answer(2,1));
        sig=A*sinc(2*pi*F*temps);
    case 4
        prompt={'Entrer amplitude'};
        name='parametre du signal';
        numlines=1;
        defaultanswer={'1'};
        
        answer=inputdlg(prompt,name,numlines,defaultanswer);
        A=str2double(answer(1,1));
        sig=A*ones(1,N);
end
s=fft(sig);
set(handles.listbox2,'string',s)
axes(handles.axes2)
plot(temps,sig,'m-')
xlabel('le temps[sec]')
ylabel('amplitude[v]')
title('signal')
axes(handles.axes3)
plot(freq,fftshift(abs(s)),'m-')
xlabel('la fréquence[Hz]')
ylabel('amplitude[v]')
title('spectre')
f=fopen('C:\Users\Othman\Desktop\tfmatlab.txt','wt');
fprintf(f,'%f \n',s);
fclose(f);
f=fopen('C:\Users\Othman\Desktop\signal.txt','wt');
fprintf(f,'%f \n',sig);
fclose(f);
open ('C:\Users\Othman\Desktop\Zakaria\S2\Programmation orienté objet\Projets\Projet matlab c++\Projet.exe');



